#!/usr/bin/env python3

number1 = int(input("Give me the first number: \n"))
number2 = int(input("Give me the second number: \n"))

print("Thank you")

print(f"{number1} + {number2} = {number1 + number2}")
print(f"{number1} - {number2} = {number1 - number2}")
print(f"{number1} / {number2} = {number1 / number2:.0f}")
print(f"{number1} * {number2} = {number1 * number2}")